const postContainers = document.querySelectorAll(".post-container");
const modal = document.querySelector(".modal");


postContainers.forEach(postContainer => {
    const sectionName = postContainer.getAttribute("section-name");

    if (sectionName in postInformation) {
        postContainer.innerHTML = '';

        for (let lesson in postInformation[sectionName]) {
            postContainer.appendChild(document.createElement("h1")).innerHTML = `${postInformation[sectionName][lesson].Lesson_heading.title}`;


            for(let lessonCols in postInformation[sectionName][lesson].Lesson_Cols) {
                let createCard = postContainer.appendChild(document.createElement("div"));
                createCard.classList.add("card");
                createCard.innerHTML = `<h2>${postInformation[sectionName][lesson].Lesson_Cols[lessonCols].Col_heading.title}`;


                let createul = createCard.appendChild(document.createElement("ul"));

                for (let Countli in postInformation[sectionName][lesson].Lesson_Cols[lessonCols].Unordered_list) {
                    let liClick = createul.appendChild(document.createElement("li"));
                    liClick.innerHTML = `${postInformation[sectionName][lesson].Lesson_Cols[lessonCols].Unordered_list[Countli].li}`;

                    liClick.addEventListener("click", () => {
                        modal.style.display = "block";
                        modal.innerHTML = `<span onclick="this.parentNode.style.display ='none'">&times;</span>${postInformation[sectionName][lesson].Lesson_Cols[lessonCols].Unordered_list[Countli].li_content}`;
                    })

                }
            }
        }
    }
})
